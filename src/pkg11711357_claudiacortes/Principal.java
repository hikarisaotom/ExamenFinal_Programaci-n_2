    private void btn_inicioActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_inicioActionPerformed
        adm_Archivo Datos = new adm_Archivo("./Examen2.txt");
        Datos.CargarArchivo();
        /*OBETENEMOS LOS VALORES DEL ARCHIVO*/
        IndicadorDia = Datos.getIndicadorDia();
        Indicador_crecimiento = Datos.getIndicador_crecimiento();
        Prom_Lluvia_Diaria = Datos.getProm_Lluvia_Diaria();
        Indicador_Porcentaje_Represas = Datos.getIndicador_Porcentaje_Represas();
        Re_1 = Datos.getRe_1();//Los Laureles.
        Re_2 = Datos.getRe_2();//La concepcion.
        Re_3 = Datos.getRe_3();//Fco. Morazan.
        /*INDICADORES DE CONSUMO*/
        In_1 = Datos.getIn_1();//Los Laureles.
        In_2 = Datos.getIn_2();//La concepcion.
        In_3 = Datos.getIn_3();//Fco. Morazan.
        lbl_dia.setText(IndicadorDia + "");
        /*CARGAMOS LOS VALORES AL ARBOL*/
        DefaultTreeModel modelo = (DefaultTreeModel) jt_Represas.getModel();
        DefaultMutableTreeNode Raiz = (DefaultMutableTreeNode) modelo.getRoot();
        DefaultMutableTreeNode nodo = new DefaultMutableTreeNode(Re_1.get(0));
        DefaultMutableTreeNode sub_nodo = new DefaultMutableTreeNode(Re_1.get(1));
        nodo.add(sub_nodo);
        DefaultMutableTreeNode nodo1 = new DefaultMutableTreeNode(Re_2.get(0));
        DefaultMutableTreeNode sub_nodo1 = new DefaultMutableTreeNode(Re_2.get(1));
        nodo1.add(sub_nodo1);
        DefaultMutableTreeNode nodo2 = new DefaultMutableTreeNode(Re_3.get(0));
        DefaultMutableTreeNode sub_nodo2 = new DefaultMutableTreeNode(Re_3.get(1));
        nodo2.add(sub_nodo2);
        Raiz.add(nodo);
        Raiz.add(nodo1);
        Raiz.add(nodo2);
        modelo.reload();
        int Consumo1 = Integer.parseInt(In_1.get(1) + "");
        int Condumo2 = Integer.parseInt(In_2.get(1) + "");
        int Consumo3 = Integer.parseInt(In_3.get(1) + "");
        //boolean Vive, boolean avanza, JProgressBar barra, int Consumo1, int Condumo2, int Consumo3

        Hc = new hiloConsumo(true, true, pb_consumoPoblacion, Consumo1, Condumo2, Consumo3, pb_Laureles, pb_Concepcion, pb_FranciscoMorazan);
        He = new HiloEnergia(true, true, pb_Generacion_Energia, Consumo1, Condumo2, Consumo3);
        double porcentaje_1 = Double.parseDouble(Re_1.get(1) + "");
        double porcentaje_2 = Double.parseDouble(Re_2.get(1) + "");
        double porcentaje_3 = Double.parseDouble(Re_3.get(1) + "");
        //System.out.println("PORCENTAJE LOS LAURELES"+porcentaje_1);
        ConsumoAno = new hiloConsumoAno(true, true, pb_lluvias2017, lbl_dia, Prom_Lluvia_Diaria, Indicador_crecimiento);
        Laureles = new HiloRepresa(true, true, pb_Laureles, pb_lluvias2017, porcentaje_1);
        Concepcion = new HiloRepresa(true, true, pb_Concepcion, pb_lluvias2017, porcentaje_2);
        Fco_Morazan = new HiloRepresa(true, true, pb_FranciscoMorazan, pb_lluvias2017, porcentaje_3);
        ConsumoAno.start();
        Laureles.start();
        Concepcion.start();
        Fco_Morazan.start();
        Hc.start();
        He.start();

    }//GEN-LAST:event_btn_inicioActionPerformed

